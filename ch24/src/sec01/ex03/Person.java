package sec01.ex03;

public class Person <A, B> {
	protected A name;
	protected B age;
	
	public A getName() {
		return name;
	}
	public void setName(A name) {
		this.name = name;
	}
	public B getAge() {
		return age;
	}
	public void setAge(B age) {
		this.age = age;
	}
	
	

}
