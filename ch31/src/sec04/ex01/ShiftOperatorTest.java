package sec04.ex01;

public class ShiftOperatorTest {
	public static void main(String[] args) {
		System.out.println("2 << 3 = " + (2 << 3));
		System.out.println("16 >> 3 = " + (16 >> 3));
		System.out.println("-16 >> 3 = " + (-16 >> 3));
		System.out.println("-16 >>> 3 = " + (-16 >>> 3));
	}
}
