package sec01.ex02;

public class IfElseTest {

	public static void main(String[] args) {
		int score = 55;

		if (score >= 60) {
			System.out.println("60점 이상입니다.");
			System.out.println("합격입니다.");
		} else {
			System.out.println("60점 미만입니다.");
			System.out.println("불합격입니다.");
		}

	}

}
