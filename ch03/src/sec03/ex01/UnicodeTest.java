package sec03.ex01;

public class UnicodeTest {

	public static void main(String[] args) {
		char ch1 = 97;
		char ch2 = 98;
		int num1 = 'a';
		int num2 = 'b';

		System.out.println("ch1 = " + ch1);
		System.out.println("ch2 = " + ch2);
		System.out.println("num1 = " + num1);
		System.out.println("num2 = " + num2);

		System.out.println("a의 아스키 코드 = " + (int)ch1);
		System.out.println("b의 아스키 코드 = " + (int)ch2);
		System.out.println("num1의 문자 = " + (char)num1);
		System.out.println("num2의 문자 = " + (char)num2);

	}
}
