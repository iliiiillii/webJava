package sec04.ex01;

public class FloatDoubleVarTest {

	public static void main(String[] args) {
		float num1 = 1.23456f;
		double num2 = 1.23456d;
		double num3 = 1.23456;
		System.out.println("num1 = " + num1);
		System.out.println("num2 = " + num2);
		System.out.println("num3 = " + num3);
		System.out.println();
//		num1=.28;
//		num2 = .28f;
		
		num1 = .28f;
		num2 = 2e+10;
		num3 = 9.26E3;
		System.out.println("num1 = " + num1);
		System.out.println("num2 = " + num2);
		System.out.println("num3 = " + num3);
	}
}
