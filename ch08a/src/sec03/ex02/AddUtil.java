package sec03.ex02;

public class AddUtil {
	
	public int add2(int x, int y) {
		return x + y;
	}
	
	public int add3(int x, int y, int z) {
		return x + y + z;
	}
	
	public float addFloat2(float x, float y) {
		return x + y;
	}
	
	public float addFloat3(float x, float y, float z) {
		return x + y + z;
	}

}
